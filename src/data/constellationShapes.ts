export const getConstellationShape = (name: string) => {
  const shape = constellationShapes[name as keyof typeof constellationShapes];
  if (shape) return shape;
  
  const starCount = 4 + Math.floor(Math.random() * 6);
  const stars = Array.from({ length: starCount }, () => ({
    x: 0.2 + Math.random() * 0.6,
    y: 0.2 + Math.random() * 0.6,
    brightness: 0.5 + Math.random() * 0.5
  }));
  
  const lines = stars.slice(0, -1).map((_, i) => ({
    from: i,
    to: i + 1
  }));
  
  if (starCount > 4 && Math.random() > 0.5) {
    lines.push({ from: 0, to: starCount - 1 });
  }
  
  return { stars, lines };
};
